//p02_11.cpp - операции сдвига и поразрядные операции
#include <iostream>

using namespace std;

int main() {
	
	cout << "\n4 << 3 PABHO " << (4 << 3);
	cout << "\n5 >> 1 PABHO " << (5 >> 1);
	cout << "\n6 & 5 PABHO  " << (6 & 5);
	cout << "\n6 | 5 PABHO  " << (6 | 5);
	cout << "\n6 ^ 5 PABHO  " << (6 ^ 5);
	
	cout << "\n19 & 11 PABHO  " << (19 & 11);
	cout << "\n19 | 11 PABHO  " << (19 | 11);
	cout << "\n19 ^ 11 PABHO  " << (19 ^ 11);
	
	cout << '\n';
	return 0;
}
