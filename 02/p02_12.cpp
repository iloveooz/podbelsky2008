//p02_12.cpp - операции отношения и логические операции
	#include <iostream>

	using namespace std;

	int main() {
		cout << "\n3<5 равно " << (3 < 5);
		cout << "\n3>5 равно " << (3 > 5);
		cout << "\n3==5 равно " << (3 == 5);
		cout << "\n3!=5 равно " << (3 != 5);
		cout << "\n3!=5 || 3==5 равно " << (3 != 5 || 3 == 5);
		cout << "\n3+4>5 && 3+5>4 && 4+5>3 равно " << (3+4>5 && 3+5>4 && 4+5>3);

		cout << endl;
		return 0;
	}
